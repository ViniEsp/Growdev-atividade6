let dia = prompt("Digite um dia da seman:")

switch (dia){
    case 1:
        console.log("Domingo")
        break
    case 2:
        console.log("Segunda")
        break
    case 3:
        console.log("terça")
    case 4:
        console.log("quarta")
        break
    case 5:
        console.log("quinta")
        break
    case 6:
        console.log("sexta")
        break
    case 7:
        console.log("sabad0")
        break
    default:
        console.log("Numero invalido")
}